#The sort() function list elements in alphabetically

random_list = ["mango","apple","pear","onion","eggs"]
random_list.sort()
print("Alphabetical sorting")
print(random_list)

#we can reverse the sorting
random_list.sort(reverse=True)
print("Reverse sorting")
print(random_list)

#we can also print from left to right
random_list.reverse
print("From left to right")
print(random_list)